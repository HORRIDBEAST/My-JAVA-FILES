public class delete_last_node {
    /****************************************************************

 Following is the class structure of the Node class:

 class Node {
     public int data;
     public Node next;

     Node()
     {
         this.data = 0;
         this.next = null;
     }

     Node(int data)
     {
         this.data = data;
         this.next = null;
     }

     Node(int data, Node next)
     {
         this.data = data;
         this.next = next;
     }
 };

 *****************************************************************/

public class Solution {
    public static Node deleteLast(Node list){
       if(list==null ||list.next==null) return null;

       Node t=list;
       while(t.next.next!=null){
           t=t.next;
       }
       t.next=null;
       return list;
    }
}
}
